export const LANGS = ["it", "en", "de"];

export let lang = localStorage.getItem("desideri101.lang")
  || (navigator.language || "it").slice(0, 2);
if (!LANGS.includes(lang)) lang = "it";

export function setLang(l) {
  if (!LANGS.includes(l)) return;
  lang = l;
  localStorage.setItem("desideri101.lang", l);
  document.documentElement.lang = l;
  document.body.className = document.body.className.replace(/\blang-\w+\b/g, "").trim() + " lang-" + l;
}

